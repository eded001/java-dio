package aulas.variaveis;

public class Declaracao {
    public static void main(String args[]) {
        String apelido = "ed";
        // a declaração ocorre da seguinte maneira:
        // <Tipo/Classe> <nome> = <valor>

        String _nome = "Edgar";
        boolean $dinheiro = false;
        int idade19 = 19;

        // a declaração é estritamente incorreta quando:
        // 1- inicia com um número
        // 2- é um termo reservado da linguagem (if, while, for...)
        // 3- contém espaços
        // 4- é repetida
        // 5- é algum outro símbolo além do cifrão, underline ou uma letra do alfabeto

        // a declaração só é correta quando:
        // 1- começa com uma letra
        // 2- começa com underline (_)
        // 3- começa com um cifrão ($)
        // 4- um número é posicionado DEPOIS de uma letra
        // 5- é uma letra do alfabeto
    }
}